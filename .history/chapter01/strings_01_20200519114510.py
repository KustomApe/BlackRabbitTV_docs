# 文字リテラルを記述する
'文字列'
"文字列"

# 文字列は英数字/空白/日本語、全角でも大丈夫です
"Hello, Python"
"myname"
"こんにちは"
"2019"

# ダブルクオーテーションとシングルクオーテーションで囲った場合で違いはありません。
